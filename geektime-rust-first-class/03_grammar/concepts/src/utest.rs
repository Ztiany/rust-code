//演示单元测试：Rust 的单元测试一般放在和被测代码相同的文件中，使用条件编译  #[cfg(test)] 来确保测试代码只在测试环境下编译。
//====================================================================

fn add(a: u32, b: u32) -> u32 {
    println!("add is run");
    a + b
}

#[cfg(test)]
mod tests {
    use crate::utest::add;

    #[test]
    fn test_add() {
        assert_eq!(add(2, 2), 4);
    }
}